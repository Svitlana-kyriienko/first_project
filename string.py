# 6. Напишіть програму, яка по введеному числу `n` від 1 до 9 виводить на екран `n` пінгвінів з відповідним номером - число від 1 до `n`. Зображення одного пінгвіна має розмір `5 x 9` символів, між двома сусідніми пінгвінами також є порожній (з пропусків) стовпець. Дозволяється вивести порожній стовпець після останнього пінгвіна. Для спрощення малювання скопіюйте пінгвіна із вихідних даних. Врахуйте, що виведення на екран виконується порядково, а не «попінгвінно».

#     Вхідні дані:
#     ```
#     4
#     ```
#     Вихідні дані:
#     ```
#        _~_        _~_        _~_        _~_
#       (o o)      (o o)      (o o)      (o o)
#      /  V  \    /  V  \    /  V  \    /  V  \
#     /(  1  )\  /(  2  )\  /(  3  )\  /(  4  )\
#       ^^ ^^      ^^ ^^      ^^ ^^      ^^ ^^
#

def task6():
    # Считываем число из ввода — количество пингвинов, которых нужно нарисовать
    number_p = int(input())

    # Строка, описывающая верхнюю часть пингвина (волнистая "макушка")
    p1 = "    _~_    "

    # Строка с глазами пингвина
    p2 = "   (o o)   "

    # Строка с клювом пингвина (символ 'V' между наклонными чертами)
    p3 = "  /  V  \\  "

    # Строка с лапками пингвина (внизу)
    p5 = "   ^^ ^^   "

    # Печатаем верхнюю часть всех пингвинов подряд (умножаем строку на количество пингвинов)
    print(p1 * number_p)

    # Печатаем глаза всех пингвинов подряд
    print(p2 * number_p)

    # Печатаем клювы всех пингвинов подряд
    print(p3 * number_p)

    # Создаём строку из цифр от 1 до 9, затем обрезаем её, чтобы длина зависела от количества пингвинов
    # (каждому пингвину примерно соответствует два символа)
    nums = "1 2 3 4 5 6 7 8 9"[:2 * number_p]

    # '''пошаговое объяснение строки кода:
    # # nums.split() — разбивает строку nums (например "1 2 3") на список ["1", "2", "3"]. То есть каждый номер пингвина становится отдельным элементом.

    # # r" )\ /( ".join(nums.split()) — соединяет эти элементы между собой строкой-разделителем " )\ /( "
    #   В итоге получается строка: "1  )\  /(  2  )\  /(  3"

    # # " /( " + ... + r" )\ " — добавляет к началу и концу общей строки недостающие части, чтобы рисунок выглядел правильно (левая и правая границы конструкции)'''

    p4 = " /(  " + r"  )\  /(  ".join(nums.split()) + r"  )\ "

    # Печатаем тела пингвинов с номерами (на этой строке появляется ошибка из-за r-строк)
    print(p4)

    # Печатаем пустую строку и нижнюю часть (лапки) всех пингвинов подряд
    print("\n" + p5 * number_p)

# task6()


# 7. У рядку є кілька слів, розділених одним або декількома пропусками. Потрібно прибрати з тексту зайві пропуски: два і більше пропусків поспіль, а також всі пропуски на початку і в кінці рядка. На вхід програмі подається рядок, що складається не більше ніж з 255 символів. Надрукувати новий рядок.

#     Вхідні дані:
#     <pre>
#        Beyond the green     swelling hills of the     Mittel Land rose mighty slopes of forest    up    to the lofty steeps of the Carpathians    themselves</pre>

#     Вихідні дані:
#     ```
#     Beyond the green swelling hills of the Mittel Land rose mighty slopes of forest up to the lofty steeps of the Carpathians themselves
#     ```

def task7():
    text = "   Beyond the green     swelling hills of the     Mittel Land rose mighty slopes of forest    up    to the lofty steeps of the Carpathians    themselves   "

    # Убираем лишние пробелы:
    # 1. text.split() — разбивает строку по любому количеству пробелов на отдельные слова.
    #    Например, ['Beyond', 'the', 'green', 'swelling', ...]
    # 2. " ".join(...) — соединяет эти слова обратно в строку, 
    #    вставляя между ними ровно один пробел.
    # В результате получаем строку без лишних пробелов.
    text_new = " ".join(text.split())

    print(text_new)

# task7()


