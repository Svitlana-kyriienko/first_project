# Tasks for lesson on the topic of "String -> `str()`"


# | Команда / Метод             | Синтаксис             | Описание                                         | Пример                                          |
# | --------------------------- | --------------------- | ------------------------------------------------ | ----------------------------------------------- |
# | **Создание строки**         | `s = "текст"`         | Объявление строки                                | `s = "Привет"`                                  |

# | **Длина строки**            | `len(s)`              | Возвращает количество символов в строке          | `len("Python") → 6`                             |

# | **Доступ по индексу**       | `s[i]`                | Возвращает символ с индексом `i` (нумерация с 0) | `"Python"[0] → 'P'`                             |

# | **Срез строки**             | `s[start:end:step]`   | Извлекает часть строки                           | `"Python"[1:4] → 'yth'`                         |

# | **Конкатенация**            | `s1 + s2`             | Объединяет две строки                            | `"Py" + "thon" → "Python"`                      |

# | **Повтор строки**           | `s * n`               | Повторяет строку `n` раз                         | `"Hi" * 3 → 'HiHiHi'`                           |

# | **Проверка подстроки**      | `'sub' in s`          | Проверяет, есть ли подстрока                     | `'Py' in 'Python' → True`                       |

# | **Преобразование регистра** | `s.lower()`           | Все символы в нижний регистр                     | `"HELLO".lower() → 'hello'`                     |
# |                             | `s.upper()`           | Все символы в верхний регистр                    | `"hi".upper() → 'HI'`                           |
# |                             | `s.title()`           | Каждое слово с заглавной буквы                   | `"python code".title() → 'Python Code'`         |

# | **Удаление пробелов**       | `s.strip()`           | Удаляет пробелы по краям                         | `"  hi  ".strip() → 'hi'`                       |

# | **Замена подстроки**        | `s.replace(old, new)` | Заменяет подстроку на другую                     | `"яблоко".replace("о", "а") → 'яблака'`         |

# | **Разделение строки**       | `s.split(sep)`        | Делит строку на список по разделителю            | `"a,b,c".split(",") → ['a','b','c']`            |

# | **Объединение строк**       | `sep.join(list)`      | Объединяет список строк через разделитель        | `",".join(['a','b','c']) → 'a,b,c'`             |

# | **Поиск подстроки**         | `s.find(sub)`         | Возвращает индекс первого вхождения              | `"Python".find("th") → 2`                       |

# | **Проверка начала/конца**   | `s.startswith(sub)`   | Проверяет, начинается ли строка с подстроки      | `"Python".startswith("Py") → True`              |
# |                             | `s.endswith(sub)`     | Проверяет, оканчивается ли строка подстрокой     | `"Python".endswith("on") → True`                |

# | **Проверка содержимого**    | `s.isdigit()`         | Проверяет, состоит ли строка из цифр             | `"123".isdigit() → True`                        |
# |                             | `s.isalpha()`         | Проверяет, только ли буквы                       | `"abc".isalpha() → True`                        |
# |                             | `s.isalnum()`         | Проверяет, буквы и/или цифры                     | `"abc123".isalnum() → True`                     |

# | **Форматирование строки**   | `f"{var}"`            | Вставка переменных в строку (f-строка)           | `name="Аня"; f"Привет, {name}" → 'Привет, Аня'` |
# |                             | `"{}".format(var)`    | Старый способ форматирования                     | `"Привет, {}".format("Аня") → 'Привет, Аня'`    |

# | **Обратный порядок**        | `s[::-1]`             | Разворачивает строку                             | `"Python"[::-1] → 'nohtyP'`                     |


