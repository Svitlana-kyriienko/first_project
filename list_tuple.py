# | Команда / Метод                   | Синтаксис    | Описание                                                                                 | Пример                                                               

# | **Создание списка**               | `lst = [элементы]`                    | Создаёт список                                                                  | `lst = [1, 2, 3]`                                                    |
# | **Длина списка**                  | `len(lst)`                            | Возвращает количество элементов                                                 | `len([1,2,3]3`                                                   |
# | **Доступ по индексу**             | `lst[i]`                     | Возвращает элемент по индексу                                                            | `[10,20,30][1] → 20`                                                 |
# | **Изменение элемента**            | `lst[i] = значение`          | Изменяет элемент по индексу                                                              | `lst[0] = 99`                                                |
# | **Срез списка**                   | `lst[start:end:step]`        | Извлекает подсписок                                                                      | `[1,2,3,4,5][1:4] → [2,3,4]`                                         |
# | **Добавление элемента**           | `lst.append(x)`                       | Добавляет элемент в конец                                                       | `lst.append(10)`                                                     |
# | **Вставка элемента**              | `lst.insert(i, x)`                    | Вставляет элемент на позицию `i`                                                | `lst.insert(1, 99)`                                                  |
# | **Расширение списка**             | `lst.extend(iterable)`                | Добавляет несколько элементов                                                   | `[1,2].extend([3,4]) → [1,2,3,4]`                                    |
# | **Удаление по значению**          | `lst.remove(x)`                       | Удаляет первое вхождение значения                                               | `[1,2,3,2].remove(2) → [1,3,2]`                                      |
# | **Удаление по индексу**           | `lst.pop(i)`                          | Удаляет элемент по индексу и возвращает его                                     | `a = [1,2,3]; a.pop(1) → 2`                                    
# | **Очистка списка**                | `lst.clear()`                         | Удаляет все элементы                                                            | `[1,2,3].clear() → []`                                               |
# | **Поиск индекса элемента**        | `lst.index(x)`                        | Возвращает индекс первого вхождения                                             | `[10,20,30].index(20) → 1`                                           |
# | **Поиск индекса в диапазоне**     | `lst.index(x, start, end)`            | Возвращает индекс `x` между позициями `start` и `end`                           | `lst = [1,2,3,2,4]; lst.index(2,2,5) → 3`                            |
# | **Подсчёт количества**            | `lst.count(x)`                        | Считает, сколько раз встречается элемент                                        | `[1,2,2,3].count(2) → 2`                                             |
# | **Метод сортировки списка**       | `lst.sort(reverse=False)`             | **Сортирует сам список на месте** (изменяет исходный)                           | `a = [3,1,2]; a.sort(); print(a) → [1,2,3]`                          |
# | **Функция сортировки списка**     | `sorted(lst, reverse=False)`          | **Возвращает новый отсортированный список**, не изменяя исходный                | `a = [3,1,2]; b = sorted(a); print(b) → [1,2,3]; print(a) → [3,1,2]` |
# | **Разница между `sort()`
# и `sorted()`** | —                  | `sort()` → изменяет исходный список<br>
#                                       `sorted()` → создаёт новый отсортированный список | —              |
# | **Обратный порядок**                | `lst.reverse()`           | Разворачивает элементы                                                                   | `[1,2,3].reverse() → [3,2,1]`                                        |
# | **Копирование списка**              | `lst.copy()`              | Создаёт поверхностную копию                                                              | `b = a.copy()`                                                       |
# | **Объединение списков**             | `lst1 + lst2`             | Складывает два списка                                                                    | `[1,2] + [3,4] → [1,2,3,4]`                                          |
# | **Повтор списка**                   | `lst * n`                             | Повторяет элементы списка `n` раз                                                        | `[1,2] * 2 → [1,2,1,2]`                                              |
# | **Проверка элемента**               | `x in lst`                            | Проверяет, есть ли элемент                                                               | `3 in [1,2,3] → True`                                                |
# | **Перебор элементов**               | `for x in lst:`                       | Проходит по всем элементам                                                               | `for i in [1,2,3]: print(i)`                                         |
# | **Списковое выражение**             | `[x**2 for x in iterable if условие]` | Создаёт новый список по шаблону                                                          | `[x**2 for x in range(5)] → [0,1,4,9,16]`                            |
# | **Минимум и максимум**              | `min(lst)`, `max(lst)`                | Возвращает минимальное/максимальное значение                                             | `min([3,1,2]) → 1`                                                   |
# | **Сумма элементов**                 | `sum(lst)`                            | Возвращает сумму числовых элементов                                                      | `sum([1,2,3]) → 6`                                                   |


